    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            // TODO add your handling code here:
            Pais pais = Wrappers.criaPais(jTextField1.getText());
            if (pais != null) {
                Document doc = XMLJDomFunctions.lerDocumentoXML("paises.xml");

                boolean existe = false;
                if (doc != null) {
                    Element raiz = doc.getRootElement();
                    for (Element e : raiz.getChildren("paisDados")) {
                        if (e.getAttributeValue("nome").equalsIgnoreCase(pais.getNome())) {
                            existe = true;
                            break;
                        }
                    }
                }

                // Se existir, mostra mensagem e sai
                if (existe) {
                    jDialog1.setVisible(false);
                    JOptionPane.showMessageDialog(this,
                            "País já existe no ficheiro",
                            "Informação",
                            JOptionPane.INFORMATION_MESSAGE);
                    return; // não adiciona
                }

                doc = ModeloXML.adicionaPais(pais, doc);
                XMLJDomFunctions.escreverDocumentoParaFicheiro(doc, "paises.xml");

                String texto = XMLJDomFunctions.escreverDocumentoString(doc);
                jTextArea1.setText(texto);
                jDialog1.setVisible(false);
                JOptionPane.showMessageDialog(this,
                        "Pais adicionado com sucesso",
                        "Informação",
                        JOptionPane.INFORMATION_MESSAGE);
            } else {
                jDialog1.setVisible(false);
                JOptionPane.showMessageDialog(this,
                        "País já existe no ficheiro",
                        "Informação",
                        JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (IOException ex) {
            Logger.getLogger(JFramePaises.class.getName()).log(Level.SEVERE, null, ex);
            jDialog1.setVisible(false);
            JOptionPane.showMessageDialog(this,
                    "País não adicionado",
                    "ERRO",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

