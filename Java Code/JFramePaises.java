    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        String nomePais = jTextField13.getText();

        Document original = XMLJDomFunctions.lerDocumentoXML("paises.xml");
        Element raizOriginal = original.getRootElement();
        Element paisSelecionado = null;

        for (Element pais : raizOriginal.getChildren("paisDados")) {
            if (pais.getAttributeValue("nome").equalsIgnoreCase(nomePais)) {
                paisSelecionado = pais.clone();  // clona para manter no novo doc
                break;
            }
        }

        if (paisSelecionado != null) {
            // 3. Cria novo documento com o país selecionado
            Element novaRaiz = new Element("paises");
            novaRaiz.addContent(paisSelecionado);
            Document docTemp = new Document(novaRaiz);

            // 4. Grava XML temporário
            XMLJDomFunctions.escreverDocumentoParaFicheiro(docTemp, "tempPais.xml");

            // 5. Aplica XSLT
            Document novo = JDOMFunctions_XSLT.transformaDocumento(docTemp, "tempPais.xml", "fronteiras.xsl");

            if (novo != null) {
                try {
                    XMLJDomFunctions.escreverDocumentoParaFicheiro(novo, "fronteiras.html");
                    Desktop.getDesktop().browse(new File("fronteiras.html").toURI());
                    jDialog9.setVisible(false);
                } catch (IOException ex) {
                    Logger.getLogger(JFramePaises.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this,
                        "Erro na transformação",
                        "Erro",
                        JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(this,
                    "País não encontrado: " + nomePais,
                    "Erro",
                    JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_jButton9ActionPerformed

